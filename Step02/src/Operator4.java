
public class Operator4 {
	/*
	 * 	관계 연산자
	 * 		>	<	>=	<=	==	!=
	 */
	public static void main(String[] args) {
		//정수형 변수 두개 선언 후 초기화
		int n1 = 10, n2 =7;
		System.out.println(n1 > n2);
		System.out.println(n1 >= n2);
		System.out.println(n1 < n2);
		System.out.println(n1 <= n2);
		System.out.println(n1 == n2);
		System.out.println(n1 != n2);
	}

}
