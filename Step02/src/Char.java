
public class Char {
	/*
	 * 	Char : 문자형 ---> 한글자만 저장하는 변수 타입
	 * 
	 */
	public static void main(String[] args) {
		char c1 = 'A';
		char c2 = 'B';
		
		System.out.println(c1);
		System.out.println(c2);
		
		int i1 = c1;
		System.out.println(i1);
		int i2 = 'Z';
		System.out.println(i2);
		
		char c3 = 'a';
		int i3 = c3;
		System.out.println(c3);
		System.out.println(i3);
		
	}

}


